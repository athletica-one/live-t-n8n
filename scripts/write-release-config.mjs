import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const DEFAULT_API_BASE_URL = "https://api.live-t.invalid";
const DEFAULT_WEB_BASE_URL = "https://app.live-t.invalid";
const apiBaseUrlInput = process.env.LIVE_T_API_BASE_URL || DEFAULT_API_BASE_URL;
const webBaseUrlInput = process.env.LIVE_T_WEB_BASE_URL || DEFAULT_WEB_BASE_URL;
const requireProdConfig = process.env.LIVE_T_REQUIRE_PROD_CONFIG === "true";

const apiBaseUrl = normalizeUrl(apiBaseUrlInput, "LIVE_T_API_BASE_URL");
const webBaseUrl = normalizeUrl(webBaseUrlInput, "LIVE_T_WEB_BASE_URL");

if (requireProdConfig) {
  assertProdUrl(apiBaseUrl, "LIVE_T_API_BASE_URL");
  assertProdUrl(webBaseUrl, "LIVE_T_WEB_BASE_URL");
}

const outputPath = path.resolve(
  __dirname,
  "..",
  "config",
  "liveTReleaseConfig.ts",
);

mkdirSync(path.dirname(outputPath), { recursive: true });
writeFileSync(
  outputPath,
  `// Auto-generated by scripts/write-release-config.mjs
// Do not edit by hand.

export const liveTApiBaseUrl = ${JSON.stringify(apiBaseUrl)};
export const liveTWebBaseUrl = ${JSON.stringify(webBaseUrl)};
export const liveTDocumentationUrl = ${JSON.stringify(
    "https://github.com/athletica-one/live-t-n8n#credentials",
  )};
`,
  "utf8",
);

function normalizeUrl(value, envName) {
  let parsed;
  try {
    parsed = new URL(value);
  } catch {
    throw new Error(`${envName} must be a valid absolute URL`);
  }

  if (!["http:", "https:"].includes(parsed.protocol)) {
    throw new Error(`${envName} must use http or https`);
  }

  parsed.hash = "";
  return parsed.toString().replace(/\/$/, "");
}

function assertProdUrl(value, envName) {
  const parsed = new URL(value);
  if (parsed.protocol !== "https:") {
    throw new Error(`${envName} must use https for release builds`);
  }
  if (value.includes(".invalid")) {
    throw new Error(`${envName} must be set for release builds`);
  }
}
